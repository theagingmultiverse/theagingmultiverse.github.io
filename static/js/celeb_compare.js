function selectCompMedia(section, element) {
    // Remove 'active' class from all pills in the current section
    const pills = document.querySelectorAll(`#scene-pills-${section} .pill`);
    pills.forEach(p => p.classList.remove('active'));

    // Add 'active' class to the clicked pill
    element.classList.add('active');

    const id = element.dataset.value;
    let data = null;

    if (section === 'celebrity') {
        data = celebrityData[id];
        document.getElementById('celebSourceImage').src = data.images.source;
        document.getElementById('celebSourceAge').innerText = data.sourceAge;
        document.getElementById('celebCondition').innerText = data.condition;
        document.getElementById('celebTargetAge').innerText = data.targetAge;
        document.getElementById('celebOursImage').src = data.images.ours;
        document.getElementById('celebRFSolverEditImage').src = data.images.rfsolveredit;
        document.getElementById('celebFlowEditImage').src = data.images.flowedit;
        document.getElementById('celebFireFlowImage').src = data.images.fireflow;
        document.getElementById('celebFadingImage').src = data.images.fading;
    } else if (section === 'non-celebrity') {
        data = nonCelebrityData[id];
        document.getElementById('nonCelebSourceImage').src = data.images.source;
        document.getElementById('nonCelebSourceAge').innerText = data.sourceAge;
        document.getElementById('nonCelebCondition').innerText = data.condition;
        document.getElementById('nonCelebTargetAge').innerText = data.targetAge;
        document.getElementById('nonCelebOursImage').src = data.images.ours;
        document.getElementById('nonCelebRFSolverEditImage').src = data.images.rfsolveredit;
        document.getElementById('nonCelebFlowEditImage').src = data.images.flowedit;
        document.getElementById('nonCelebFireFlowImage').src = data.images.fireflow;
        document.getElementById('nonCelebFadingImage').src = data.images.fading;
    }
}

// Initialize the first celebrity on page load
document.addEventListener('DOMContentLoaded', () => {
    const initialCelebPill = document.querySelector('#scene-pills-celebrity .pill.active');
    if (initialCelebPill) {
        selectCompMedia('celebrity', initialCelebPill);
    }
    const initialNonCelebPill = document.querySelector('#scene-pills-non-celebrity .pill.active');
    if (initialNonCelebPill) {
        selectCompMedia('non-celebrity', initialNonCelebPill);
    }
}); 